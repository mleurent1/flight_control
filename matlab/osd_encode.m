function d = osd_encode(str)

t = str - ' ' + 32; % ASCII conversion
d = t;
d((t==48)) = 10; % '0'
d((t>=49)&(t<=57)) = d((t>=49)&(t<=57)) - 49 + 1; % 1-9
d((t>=65)&(t<=90)) = d((t>=65)&(t<=90)) - 65 + 11; % A-Z
d((t>=97)&(t<=122)) = d((t>=97)&(t<=122)) - 97 + 2*16+5; % a-z
d((t==32)) = 0; % ' '
d((t==45)) = 4*16+9; % '-'
d((t==46)) = 4*16+1; % '.'
d((t==58)) = 4*16+4; % ':'

end